        <h5 class="sub-contit">Field Trip Overview (Target habitat type, Field trip special features, etc.)</h5>
    </div>
    <div class="con-wrap">
        <div class="text-wrap">
            The National Marine Biodiversity Institute of Korea (MABIK) invites you to explore the diverse aquatic habitats of Janghang, Seocheon. This region provides access to wetlands, tidal flats, and estuarine areas with salinity levels ranging from 0.1 to 30 PSU. Due to tidal fluctuations, it offers opportunities to collect freshwater, brackish, and marine species. <br>
            Researchers can sample not only planktonic species but also benthic microalgae, euryhaline organisms, interstitial species in fine sandy mud, and anaerobic microorganisms. Key groups such as green algae, diatoms, dinoflagellates, and ciliates are abundant, making this site an excellent location for protistology studies. <br>
            Researchers can also visit SeqQrium, a specialized marine life exhibition hall, and observe state-of-the-art marine biological storage facilities. This visit presents a valuable opportunity for research, conservation studies, and academic collaboration in a unique coastal environment.
