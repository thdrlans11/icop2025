    <h4 class="sub-tit">Introduction of the Korean Society of Protistologists (KSOP)</h4>
</div>
<p>
    The Korean Society Of Protistologists (KSOP) began in 1992 but partly took a rest for about 10 years. In 2011, the KSOP actively re-started for the Asian Conference of Protistology (ACOP 2011) held in Jeju Island, Korea. Furthermore, the KSOP organized an international joint meeting with the Japan Society of Protistology (JSP) every two years. The KSOP has currently enrolled about 130 members who cover almost all directions of protistological fields. About 50 members participated in the KSOP annual meeting. In 2020, the KSOP annual meeting will be replaced with the 3rd joint meeting of the KSOP and JSP held in Kobe, Japan. The KSOP is a small but strong society and is composed of many progressive protistologists based on the foundation of experience. Compared to a short history of the protistology in Korea, the KSOP has considerable strength and breadth in Protistology. The KSOP is pledged to success the upcoming ICOP (the 16th International Congress of Protistology) meeting in Seoul, Republic of Korea from June 21st-25th in 2025. We are looking forward to seeing you at the 16th ICOP meeting in Seoul 2025.
</p>
