                    <p>Genome, Organelle and Protein Evolution</p> <br>

                    <strong><u>Research:</u></strong> <br>
                    <strong>Determining the "super-kingdoms" of eukaryotic organisms by phylogenomics.</strong>
                    <p>Using genomic surveys and molecular genetic methods, we isolate genes coding for multiple highly conserved proteins (ribosomal proteins, cpn60, hsp70 and hsp90, elongation factors, tubulins, RNA polymerases etc.) from diverse aerobic and anaerobic, parasitic and free-living protists. We do computer-based phylogenetic analyses of these sequences to: reconstruct ancient relationships amongst the unicellular protistan eukaryotes and other major eukaryotic groups. In particular we are investigating a hypothetical eukaryote super-kingdom called the 'Excavata' that is proposed to contain organisms such as Giardia, Trichomonas, trypanosomes and a variety of aerobic and anaerobic flagellates and amoebae.</p>

                    <strong>Comparative genomics of unicellular eukaryotes</strong> <br>
                    <p>Through EST surveys, we characterize the highly expressed genes from a wide variety of protistan genomes. We are specifically interested in the gene content of anaerobic protists, that have acquired genes by lateral gene transfer (LGT) from Eubacteria, Archaebacteria and Eukaryotes. By studying LGT in these organisms we are gaining an understanding of how both parasitic and free-living unicellular eukaryotic genomes change over time.</p>

                    <strong>The biology and evolution of mitochondria, hydrogenosomes and mitosomes</strong> <br>
                    <p>Mitochondria are derived from an ancient endosymbiosis of a eubacterium in eukaryotic cells. Although all known eukaryotes appear to have diverged from each other since this event, many unicellular eukaryotes that live in oxygen-poor habitats (e.g. the human gut or anoxic fresh-water environments) lack aerobic mitochondrial functions and instead have transformed their mitochondria into hydrogen-producing 'hydrogenosomes or reduced mitosomes. We are interested in characterizing the functional biodiversity of these alternatives to mitochondria and understanding the mechanisms by which they have evolved.</p>

                    <strong>Modeling the evolution of genes and genomes</strong> <br>
                    <p>We collaborate extensively with statisticians and computer scientists to develop stochastic models of gene, protein and genome evolution. We are particularly interested in creating models that capture the dynamic nature of molecular evolution over the 3.5 billion years of divergence in the tree of life.</p> <br>

